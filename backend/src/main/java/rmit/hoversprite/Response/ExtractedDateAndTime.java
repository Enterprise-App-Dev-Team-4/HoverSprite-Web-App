package rmit.hoversprite.Response;

public class ExtractedDateAndTime {
    private String date;
    private String time;

    public ExtractedDateAndTime() {}

    // Getter for date
    public String getDate() {
        return date;
    }

    // Setter for date
    public void setDate(String date) {
        this.date = date;
    }

    // Getter for time
    public String getTime() {
        return time;
    }

    // Setter for time
    public void setTime(String time) {
        this.time = time;
    }
}
